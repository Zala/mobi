package org.uninova.mobis.pojos;

/**
 * POJO NominatimAddress
 * @author PAF@UNINOVA
 */
public class NominatimAddress {

	private String houseNumber ;	// The address' house number
	private String road ;			// The address' road name
	private String suburb ;			// The address' suburb name
	private String village ;		// The address' village name
	private String town ;			// The address' town name
	private String city ;			// The address' city name
	private String county ;			// The address' county name
	private String stateDistrict ;	// The address' state district
	private String state ;			// The address' state
	private String postCode ;		// The address' postal code
	private String country ;		// The address' country name
	private String coutryCode ;		// The address' country code
	private String continent ;		// The address' continent name
	
	/**
	 * NominatimAddress Class Constructor
	 */
	public NominatimAddress() {}

	public String getHouseNumber() {
		return houseNumber;
	}

	public void setHouseNumber(String houseNumber) {
		this.houseNumber = houseNumber;
	}

	public String getRoad() {
		return road;
	}

	public void setRoad(String road) {
		this.road = road;
	}

	public String getSuburb() {
		return suburb;
	}

	public void setSuburb(String suburb) {
		this.suburb = suburb;
	}

	public String getVillage() {
		return village;
	}

	public void setVillage(String village) {
		this.village = village;
	}

	public String getTown() {
		return town;
	}

	public void setTown(String town) {
		this.town = town;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getStateDistrict() {
		return stateDistrict;
	}

	public void setStateDistrict(String stateDistrict) {
		this.stateDistrict = stateDistrict;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getPostCode() {
		return postCode;
	}

	public void setPostCode(String postCode) {
		this.postCode = postCode;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

	public String getCoutryCode() {
		return coutryCode;
	}

	public void setCoutryCode(String coutryCode) {
		this.coutryCode = coutryCode;
	}

	public String getContinent() {
		return continent;
	}

	public void setContinent(String continent) {
		this.continent = continent;
	}
	
	
}
